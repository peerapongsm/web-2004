/** Validates entries: unique non-empty ids, all required fields present. Returns human-readable errors. */
export function validateAnnotations(entries: Annotation[]): string[] {
  const errors: string[] = [];
  const seen = new Set<string>();

  for (const entry of entries) {
    if (!entry.id || !entry.id.trim()) {
      errors.push("entry missing id");
    } else if (seen.has(entry.id)) {
      errors.push(`duplicate id: ${entry.id}`);
    } else {
      seen.add(entry.id);
    }

    const requiredFields: (keyof Annotation)[] = ["title", "what", "why", "whyItDied"];
    for (const field of requiredFields) {
      if (!entry[field] || !entry[field].trim()) {
        errors.push(`${entry.id || "(no id)"}: missing ${field}`);
      }
    }
  }

  return errors;
}

export function getAnnotation(id: string): Annotation | undefined {
  return annotations.find((a) => a.id === id);
}
